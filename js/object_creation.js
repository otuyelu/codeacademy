// Arrays and Objects in JS 12/17
//  Object creation and population

// Literal syntax
var object1 = {
    position: "second",
    effect: false,
    height: 20
};

// Object constructor
var object1 = new Object();
object1.position = "first";
object1.effect = true;
object1.height = 10;



var object3 = {};
object3['position'] = "third";
object3['effect'] = true;
object3['height'] = 30;
